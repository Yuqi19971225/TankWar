public class TestDemo {
    /**
     * 没有参数的方法
     */
    public void a(){
        System.out.println("hello woeld");
    }
    /**
     * 无返回值的有参函数
     * @param b1
     */
    public void b(int b1){
        System.out.println(b1);
    }
    /**
     * 有参数的有返回值的函数
     * @param x
     * @param y
     * @return
     */
    public int c(int x,int y){
        return x+y;
    }
}
